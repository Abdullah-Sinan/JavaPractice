package day_18;

public class WhileLoop {

	public static void main(String[] args) {
		// Dry Principle
		
		int x = 10 ;
		if(x>5) {
			System.out.println("DO THIS");
		}else {
			System.out.println("DO THAT");
		}
		/*
		 * while(as long as some condition is true){
		 * repeat this action
		 * 
		 * }
		 * 
		 * 
		 */
		int y = 10 ;
		
		while ( y >5 ) {
			
			//y -- ;9-8-7-6-5
			System.out.println( "do this " + y); //y -- ;10-9-8-7-6-5
			//y -- ;10-9-8-7-6-5
			
		}

	}

}
